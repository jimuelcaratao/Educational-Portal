            <p class="pb-6">While the cuts and colors of the suit were dictated by highly specific fashion trends, the basic elements remained unchanged for centuries. The greatest revolution in workwear occurred at the start of the Industrial Revolution. When the cotton gin became widely used in the late 1700s, the sheer availability of cloth increased dramatically. In the 1820s, tailors began to offer "ready-made" menswear rather than custom-made. Other inventions accelerated the production and distribution of patterned and dyed fabric. Although work clothing was plentiful, the prevailing wisdom for menswear reflected the Victorian era's proper mores. The somber colors and austere cuts of coats and trousers were dictated by a rejection of physical pleasure and individuality and a celebration of restraint and rational use of resources.</p>

            <p class="pb-6">Working women were few and far between in nineteenth-century workplaces, as a woman's place was widely regarded as being in the home. For the sake of a graceful silhouette, the era's tight corsets and oodles of crinoline (made of itchy horsehair) made sitting uncomfortable. It was difficult to breathe with whalebone stays pressing into the ribs (no wonder women frequently suffered from "the vapors"), and steel cage hoops made skirts so wide they could barely fit through a door.
            </p>

            <p class="pb-6">The Victorian era came to an end with the death of its eponymous monarch in 1901. The rapidly changing fashion of the time in Europe and the US reflected the dramatic changes in social customs that occurred over the next few decades. Men continued to dress solemnly to reflect the formality and strength of character that came with aspiring to be a captain of industry. The European preference for frock coats worn over a vest with a watch fob, striped trousers, and a top hat contained subtle cues as to the wearer's social standing. The stripes and weaves of fabrics denoted their means and contrasted sharply with the "leisure suit," which was originally intended to be worn at home but became popular in the workplace because, at less than $30 per suit, they were a more affordable option for office dressing.
            </p>

            <p class="pb-6">As more women entered the workforce, they were given strict guidelines about what was appropriate. A feminine version of the suit with coordinated hats, gloves, bags, and shoes became popular. Dark colors were recommended by fashion magazines to hide stains.
            </p>

            <p class="pb-6">Over the next 50 years, necessity became the mother of inventive work wear. Wristwatches issued by the army during World War I eliminated the need for a watch fob or a vest pocket for the fob to be placed in. To save fabric during World War II, men's jackets were slimmed down from double-breasted to single-breasted, with narrow trousers. French designer Christian Dior popularized full billowy skirts as a reaction to the restraint of wartime fashion. Feminist critics objected to some women wanting to emulate this ultra feminine silhouette. In Louisville, 1,265 women signed an anti-Dior petition as members of the Little Below the Knee Club, believing that the New Look was not only impractical but also anti-feminist.

            </p>

            <p class="pb-6">In the meantime, suits became slimmer and less colorful. Brooks Brothers' natural-shoulder "Ivy League" look was popular among businessmen. John F. Kennedy took it a step further by wearing a two-button suit in a televised debate with Richard Nixon, effectively making three-button suits obsolete. He added to his lack of formality by not wearing a hat to his inauguration. The Oleg Cassini suits worn by First Lady Jackie Kennedy were also widely copied.

            </p>

            <p class="pb-6">As the norms shifted again, the offices of the 1960s and 1970s were a riot of bright patterns, ethnic influences, and acres of polyester and other synthetics. Arrow, the largest shirt maker at the time, sold approximately 80% of all colors except white. Diane von Furstenberg patterned wrap dresses became famous for their ability to take a working woman from office to after-hours events.

            </p>

            <p class="pb-6">This resulted in the supplication of the office, where conspicuous consumption reigned supreme. Everyone wore massive power suits, from CEOs to working girls from the suburbs. When Donna Karan and Giorgio Armani both broke the armor of the '80s with their softer sartorial approaches to office attire, the ebb and flow practically demanded a backlash. That paved the way for business casual, the ultimate substitute for suits. Some argue that The Gap and Levi's clever marketing campaigns made wearing khakis and jeans to work acceptable, at least on Fridays. Bing Gordon, cofounder of Electronic Arts, told Fortune in 1994, "If you don't have anything to say, wear a suit." And the "suits," once moguls and mavens, were now considered Luddites.
            </p>

            <p class="pb-6">Of course, with the rise of young founders commandeering successful tech companies, an increase in remote work and flexible hours, and the freelance economy, almost anything is possible. Hoodies, soccer slides, and graphic t-shirts, which were once reserved for the evenings or weekends, are now the norm for those who spend long hours in front of a computer screen. Flip-flops are more commonly seen on the feet of office workers during the summer, as employers encourage their employees to "come as they are."
            </p>

