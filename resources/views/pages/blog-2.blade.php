                THE REMINISCE OF FASHION
              </h2>
            <p class="pb-6">"You are what you wear," as the quote goes. Fashion is another way to express yourself to others. When it comes to office fashion attire, it is important to look presentable when you go to work. It makes a good impression not only on your boss, but also on other people in your workplace.People follow fashion trends in order to stay up to date on current trends. </p>

            <p class="pb-6">In this article, we will look back at past fashion trends to get a sense of what is popular among different generations.</p>

            <p class="pb-6">Changes in working attire first appeared in the European world in the 1600s. That is when lawyers, civil servants, and other professionals in London, Amsterdam, and Paris began working from offices for the first time. Along with this trend, menswear evolved into a modern suit. Of course, this was only a rough approximation of what we now know. Trousers were breeches that reached to the knee, a shirt frosted with lace at the cuffs and throat (the latter to serve as a tie), the vest was a doublet that showed off the shirt, and a cape completed the look. The overall design allowed for ease of movement regardless of the task at hand, while fine fabrics and ornamentation easily conveyed wealth and position.
            </p>

